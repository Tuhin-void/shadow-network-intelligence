@router.get("/benchmark/quantitative")
def quantitative_comparison() -> dict[str, Any]:
    """Real per-pipeline aggregate metrics from the latest benchmark run.

    If the run contains evaluations (judge + entity + semantic scoring),
    the per-pipeline aggregates ALSO include:
      • avg_judge_overall, avg_judge_hallucination, avg_judge_relevance, …
      • judge_pass_rate, hallucination_resistance_rate
      • avg_entity_f1, avg_entity_precision, avg_entity_recall
      • avg_semantic_score, semantic_method
      • avg_cost, total_cost
    """
    if not _BENCH_RESULTS.exists():
        raise HTTPException(
            status_code=404,
            detail=(
                "No 3-pipeline benchmark runs found at "
                f"{_BENCH_RESULTS.relative_to(_PROJECT_ROOT)}. "
                "Trigger one via POST /benchmark/run or via CLI: "
                "python -m 2_baseline_systems benchmark --profile small --limit 5"
            ),
        )
    runs = sorted(
        (p for p in _BENCH_RESULTS.glob("benchmark_RUN_*.json")),
        key=lambda p: p.stat().st_mtime,
        reverse=True,
    )
    if not runs:
        raise HTTPException(status_code=404, detail="no benchmark runs")
    latest = runs[0]
    try:
        run = json.loads(latest.read_text())
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"could not parse run: {e}")

    return _shape_quantitative(run)


