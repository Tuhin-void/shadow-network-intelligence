"""
Benchmark API — exposes REAL benchmark artifacts AND in-process live runs.

Two surfaces:

  1.  ARTIFACT-BACKED endpoints — read the JSON produced by the CLI scripts:
      • scripts/adversarial_benchmark.py    →  adversarial_results.json
      • scripts/benchmark_reliability.py    →  benchmark_reliability.json
      • scripts/tigergraph_validate.py      →  tigergraph_validation.json
      Returns HTTP 404 with the reproducibility command when missing.

  2.  LIVE-EXECUTION endpoints — drive the same `BenchmarkRunner` that the
      CLI uses, in-process, through `BenchmarkService`. Every metric goes
      through the same code path — no synthesized numbers.

      • POST /benchmark/run             — synchronous (small limits)
      • POST /benchmark/run/stream      — SSE event stream of run progress
      • GET  /benchmark/runs            — list all persisted runs on disk
      • GET  /benchmark/runs/{run_id}   — full detail for one run
      • GET  /benchmark/service/status  — provider config + busy flag
"""
from __future__ import annotations

import json
import logging
from pathlib import Path
from typing import Any, Optional

from fastapi import APIRouter, HTTPException
from fastapi.responses import StreamingResponse
from pydantic import BaseModel, Field

from orchestration.activation_gate import require_activation

logger = logging.getLogger(__name__)
router = APIRouter()

_PROJECT_ROOT = Path(__file__).resolve().parents[2]
_SCRIPTS = _PROJECT_ROOT / "scripts"
_BENCH_RESULTS = _PROJECT_ROOT / "2_baseline_systems" / "outputs" / "benchmark_results"

# Lazy singleton — built on first /benchmark/run* call. Heavy.
_SERVICE = None


def _service():
    global _SERVICE
    if _SERVICE is None:
        # Local import keeps the module light when nobody calls a live run.
        from orchestration.benchmark_service import BenchmarkService
        _SERVICE = BenchmarkService(profile="small")
    return _SERVICE


def _read_json(p: Path) -> dict[str, Any] | None:
    if not p.exists():
        return None
    try:
        return json.loads(p.read_text())
    except Exception as e:
        logger.warning("failed to read %s: %s", p, e)
        return None


