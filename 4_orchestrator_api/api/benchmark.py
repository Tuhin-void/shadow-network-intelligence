# ── LIVE EXECUTION ──────────────────────────────────────────────────────


class BenchmarkRunRequest(BaseModel):
    approaches: Optional[list[str]] = Field(
        default=None,
        description="Subset of ['pure_llm','vector_rag','graph_rag']. Defaults to all three.",
    )
    limit: int = Field(default=3, ge=1, le=20)
    tier: Optional[int] = Field(default=None, ge=1, le=4)
    with_scoring: bool = Field(
        default=True,
        description="Run LLM-judge + entity-match + semantic scoring on each result.",
    )


class BenchmarkAdHocRequest(BaseModel):
    question: str = Field(..., description="The analyst's natural-language query")
    approaches: Optional[list[str]] = Field(
        default=None,
        description="Subset of ['pure_llm','vector_rag','graph_rag']. Defaults to all three.",
    )
    with_scoring: bool = Field(
        default=False,
        description="Score with judge + entity + semantic (no ground truth — partial signal only).",
    )


@router.get("/benchmark/service/status")
def benchmark_service_status() -> dict[str, Any]:
    """Provider config, busy flag, and last run id. Cheap to call."""
    return _service().status()


@router.post("/benchmark/ad-hoc")
def benchmark_ad_hoc(body: BenchmarkAdHocRequest) -> dict[str, Any]:
    """Run a single analyst question through PureLLM / VectorRAG / GraphRAG.

    No ground truth is expected — this is causally tied to whatever
    investigation the analyst is running, not to the curated benchmark
    suite. Returns the full BenchmarkRun dict + the quantitative aggregates
    so the UI can show side-by-side cost, retrieval ms, source count,
    and (optionally) judge / semantic signals.

    Requires an activated environment — returns 409 when activation.kind
    == "empty". Read-only artifact endpoints (/benchmark/summary, etc.)
    remain available so evaluators can inspect prior runs without
    activating an environment.
    """
    require_activation(operation="benchmark_ad_hoc")
    try:
        run = _service().run_ad_hoc(
            question=body.question,
            approaches=body.approaches or [],
            with_scoring=body.with_scoring,
        )
    except RuntimeError as e:
        if str(e) == "benchmark_busy":
            raise HTTPException(status_code=409, detail="another benchmark run is in progress")
        raise HTTPException(status_code=500, detail=str(e))
    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e))
    except Exception as e:
        logger.exception("ad-hoc benchmark failed")
        raise HTTPException(status_code=500, detail=str(e))
    return {
        "run":          run,
        "quantitative": _shape_quantitative(run),
    }


@router.post("/benchmark/run")
def benchmark_run(body: BenchmarkRunRequest) -> dict[str, Any]:
    """Synchronous live benchmark run. Returns the persisted BenchmarkRun
    serialized + the quantitative aggregates for the same file.

    Requires an activated environment (see /benchmark/ad-hoc).
    """
    require_activation(operation="benchmark_run")
    try:
        run = _service().run(
            approaches=body.approaches or [],
            limit=body.limit,
            tier=body.tier,
            with_scoring=body.with_scoring,
        )
    except RuntimeError as e:
        if str(e) == "benchmark_busy":
            raise HTTPException(status_code=409, detail="another benchmark run is in progress")
        raise HTTPException(status_code=500, detail=str(e))
    except Exception as e:
        logger.exception("benchmark run failed")
        raise HTTPException(status_code=500, detail=str(e))
    return {
        "run":          run,
        "quantitative": _shape_quantitative(run),
    }


@router.post("/benchmark/run/stream")
def benchmark_run_stream(body: BenchmarkRunRequest):
    """SSE stream of run events. Yields:
      run.started · query.completed · query.failed · run.completed · run.error
    Each event is a JSON payload in a `data:` line.

    Requires an activated environment (see /benchmark/ad-hoc).
    """
    require_activation(operation="benchmark_run_stream")
    svc = _service()

    def _gen():
        try:
            for ev in svc.run_stream(
                approaches=body.approaches or [],
                limit=body.limit,
                tier=body.tier,
                with_scoring=body.with_scoring,
            ):
                payload = json.dumps(ev, default=str)
                kind = ev.get("kind", "event")
                yield f"event: {kind}\ndata: {payload}\n\n"
            yield "event: stream.end\ndata: {}\n\n"
        except Exception as e:
            err = json.dumps({"error": str(e), "type": type(e).__name__})
            yield f"event: error\ndata: {err}\n\n"

    return StreamingResponse(
        _gen(),
        media_type="text/event-stream",
        headers={"Cache-Control": "no-cache", "X-Accel-Buffering": "no"},
    )


@router.get("/benchmark/runs")
def list_runs() -> dict[str, Any]:
    """List all persisted runs on disk (newest first), with size + mtime."""
    if not _BENCH_RESULTS.exists():
        return {"runs": [], "directory": str(_BENCH_RESULTS.relative_to(_PROJECT_ROOT))}
    runs = sorted(
        (p for p in _BENCH_RESULTS.glob("benchmark_RUN_*.json")),
        key=lambda p: p.stat().st_mtime,
        reverse=True,
    )
    out: list[dict[str, Any]] = []
    for p in runs[:50]:  # cap response size
        try:
            run = json.loads(p.read_text())
        except Exception:
            continue
        out.append({
            "run_id":       run.get("run_id"),
            "timestamp":    run.get("timestamp"),
            "profile":      run.get("profile"),
            "queries_run":  run.get("queries_run", 0),
            "approaches":   sorted((run.get("results") or {}).keys()),
            "has_evaluations": bool(run.get("evaluations")),
            "size_bytes":   p.stat().st_size,
            "mtime":        p.stat().st_mtime,
        })
    return {
        "runs":      out,
        "directory": str(_BENCH_RESULTS.relative_to(_PROJECT_ROOT)),
        "count":     len(out),
    }


@router.get("/benchmark/runs/{run_id}")
def get_run(run_id: str) -> dict[str, Any]:
    """Full detail for a single persisted run, including evaluations if any."""
    safe_id = "".join(c for c in run_id if c.isalnum() or c in "._-")
    if safe_id != run_id:
        raise HTTPException(status_code=400, detail="invalid run_id")
    p = _BENCH_RESULTS / f"benchmark_{safe_id}.json"
    if not p.exists():
        raise HTTPException(status_code=404, detail=f"run {safe_id} not found")
    try:
        run = json.loads(p.read_text())
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"could not parse run: {e}")
    return {
        "run":          run,
        "quantitative": _shape_quantitative(run),
    }


# ── Internal: shape one BenchmarkRun JSON into the quantitative response ─


def _shape_quantitative(run: dict) -> dict[str, Any]:
    """Build the same payload `/benchmark/quantitative` returns from a
    BenchmarkRun dict. Reused by `/benchmark/run` and `/benchmark/runs/{id}`."""
    results = run.get("results") or {}
    evaluations = run.get("evaluations") or {}
    pipelines: list[dict[str, Any]] = []

    for approach in ("pure_llm", "vector_rag", "graph_rag"):
        rows = results.get(approach) or []
        evs = evaluations.get(approach) or []
        if not rows:
            pipelines.append({"approach": approach, "queries": 0})
            continue
        n = len(rows)
        sum_total = sum(int(r.get("total_tokens") or 0) for r in rows)
        sum_prompt = sum(int(r.get("prompt_tokens") or 0) for r in rows)
        sum_compl = sum(int(r.get("completion_tokens") or 0) for r in rows)
        sum_lat = sum(float(r.get("latency_ms") or 0) for r in rows)
        sum_ret = sum(float(r.get("retrieval_ms") or 0) for r in rows)
        sum_src = sum(len(r.get("sources") or []) for r in rows)
        sum_cost = sum(float(r.get("cost_estimate") or 0) for r in rows)
        errors = sum(1 for r in rows if r.get("error"))

        agg: dict[str, Any] = {
            "approach":             approach,
            "queries":              n,
            "errors":               errors,
            "avg_total_tokens":     round(sum_total / n, 1),
            "avg_prompt_tokens":    round(sum_prompt / n, 1),
            "avg_completion_tokens": round(sum_compl / n, 1),
            "avg_latency_ms":       round(sum_lat / n, 1),
            "avg_retrieval_ms":     round(sum_ret / n, 1),
            "avg_sources_retrieved": round(sum_src / n, 2),
            "total_cost":           round(sum_cost, 6),
            "avg_cost":             round(sum_cost / n, 6),
        }

        # If we have evaluations, add scoring aggregates. All numerics are
        # rounded and the source-count (n_evaluated) is exposed so the UI can
        # be honest about which scores covered how many queries.
        if evs:
            agg.update(_score_aggregates(evs))

        pipelines.append(agg)

    # Cross-reference with the adversarial JSON for structural recovery.
    adv = _read_json(_SCRIPTS / "adversarial_results.json")
    structural = None
    if adv:
        a = adv.get("aggregate") or {}
        structural = {
            "queries":                adv.get("query_count"),
            "graph_rag":              a.get("queries_with_structural_evidence", 0),
            "vector_rag":             a.get("vectorrag_structural_total", 0),
            "pure_llm":               a.get("pure_llm_structural_total", 0),
            "total_structural_edges": a.get("total_structural_edges", 0),
            "total_neighbors":        a.get("total_neighbors_traversed", 0),
        }

    # Token reduction vs the heaviest pipeline.
    max_tokens = max((p.get("avg_total_tokens") or 0) for p in pipelines) or 1
    for p in pipelines:
        avg = p.get("avg_total_tokens") or 0
        p["token_reduction_pct_vs_heaviest"] = round(
            (1 - avg / max_tokens) * 100, 1) if avg else 0

    semantic_methods = sorted({
        ev.get("semantic_method", "")
        for evs in evaluations.values()
        for ev in evs
        if ev.get("semantic_method")
    })

    return {
        "run_id":      run.get("run_id"),
        "timestamp":   run.get("timestamp"),
        "profile":     run.get("profile"),
        "config":      run.get("config"),
        "queries_run": run.get("queries_run"),
        "pipelines":   pipelines,
        "structural":  structural,
        "scoring": {
            "enabled":          bool(evaluations),
            "semantic_methods": semantic_methods,
        },
        # Retrieval-context inventory + provider transparency. These let
        # the UI honestly explain VectorRAG behaviour even when the
        # provider is mock (no actual vector search), and surface the
        # post-enrichment corpus footprint.
        "retrieval_context": _retrieval_context(run),
        # Latency contextualization. Cold sweeps are NOT operational
        # latency — the UI uses this so it doesn't misrepresent the
        # benchmark sweep duration as a steady-state user latency.
        "latency_context": {
            "sweep_mode":          "cold" if (run.get("queries_run") or 0) > 0 else "warm",
            "explanation":         (
                "Benchmark sweeps run every query through a cold engine "
                "path so each measurement is independent. Steady-state "
                "operational latency is <50ms warm-cache (see /investigate "
                "in the orchestrator)."
            ),
            "cold_avg_retrieval_ms": _avg_field(pipelines, "avg_retrieval_ms"),
            "warm_replay_ms":        "<50ms (orchestrator ResultCache hit)",
        },
        "disclosure": {
            "latency_ms_is_mock_llm":
                "avg_latency_ms reflects the LLM call which is mock unless "
                "a real provider is configured. avg_retrieval_ms is the real "
                "measured retrieval cost.",
            "latency_is_cold_sweep":
                "Benchmark sweep latency is COLD by design (each query runs "
                "fresh). Real operational latency is <50ms on the warm-cache "
                "path. Do not interpret avg_retrieval_ms as steady-state.",
            "tokens_are_real":
                "Token counts come from actual prompt construction even with "
                "mock LLM — they measure exactly what each pipeline injects "
                "into the LLM context.",
            "sources_show_structural_only":
                "avg_sources_retrieved counts STRUCTURAL items each pipeline "
                "returned as evidence. For VectorRAG with mock provider this "
                "is 0 — see `retrieval_context.semantic_corpus_size` for the "
                "actual corpus VectorRAG would index against in production.",
            "cost_is_estimate":
                "total_cost / avg_cost are estimates derived from "
                "TokenTracker._get_pricing(model). Zero for mock / open-source models.",
            "judge_is_llm":
                "avg_judge_* and judge_pass_rate come from LLMJudge — a real "
                "LLM evaluation against the answer + context. Disabled when "
                "with_scoring=False.",
            "entity_match_is_regex":
                "avg_entity_f1 / precision / recall come from EntityMatcher — "
                "regex-extracted entity IDs vs the query's ground_truth_entities.",
            "semantic_method":
                "semantic_methods lists every method used. 'bertscore' = true "
                "BERTScore F1 (requires bert_score installed); 'embedding_cosine' "
                "= cosine similarity between answer embedding and a synthesized "
                "reference text. NEVER silently swapped.",
            "structural_source":
                "Structural-recovery counts are from scripts/adversarial_results.json.",
        },
    }


def _retrieval_context(run: dict) -> dict[str, Any]:
    """Surface what each pipeline would index against in production.

    The benchmark service defaults to mock providers for deterministic
    sweeps. The UI uses this block to show 'VectorRAG would index N
    semantic chunks' even when the live sweep returned 0 sources via
    mock — that's the honest story."""
    cfg = run.get("config") or {}
    profile = run.get("profile") or "small"
    vec_provider = cfg.get("vector_provider", "mock")
    llm_provider = cfg.get("llm_provider", "mock")

    # Read the enriched corpus manifest if present.
    enriched_manifest = _PROJECT_ROOT / "outputs" / profile / "enriched_corpus" / "manifest.json"
    semantic_corpus_size = None
    enrichment_token_count = None
    enriched_doc_types = None
    if enriched_manifest.exists():
        try:
            m = json.loads(enriched_manifest.read_text())
            semantic_corpus_size = m.get("total_docs")
            enrichment_token_count = m.get("total_tokens_est")
            enriched_doc_types = list((m.get("doc_counts") or {}).keys())
        except Exception:
            pass

    return {
        "profile":              profile,
        "vector_provider":      vec_provider,
        "llm_provider":         llm_provider,
        "graph_provider":       cfg.get("graph_provider", "tigergraph"),
        "embedder_provider":    cfg.get("embedder_provider", "mock"),
        "vectorrag_mode":       (
            "real_chroma_search" if vec_provider == "chroma"
            else "mock (deterministic, returns no results)"
        ),
        "semantic_corpus_size": semantic_corpus_size,
        "enrichment_token_count": enrichment_token_count,
        "enriched_doc_types":   enriched_doc_types,
        "graphrag_mode":        "live_tigergraph_traversal",
    }


def _avg_field(pipelines: list[dict], key: str) -> float:
    vals = [float(p.get(key) or 0) for p in pipelines if p.get("queries")]
    return round(sum(vals) / len(vals), 1) if vals else 0.0


def _score_aggregates(evs: list[dict]) -> dict[str, Any]:
    n = len(evs)
    if n == 0:
        return {}

    def _avg(key: str, sub: str = None) -> float:
        if sub:
            vals = [
                float((ev.get(key) or {}).get(sub, 0.0))
                for ev in evs
                if isinstance(ev.get(key), dict)
            ]
        else:
            vals = [float(ev.get(key) or 0.0) for ev in evs]
        return round(sum(vals) / len(vals), 4) if vals else 0.0

    def _avg_entity(metric: str) -> float:
        vals = [
            float((ev.get("entity_match") or {}).get(metric, 0.0))
            for ev in evs
            if isinstance(ev.get("entity_match"), dict)
        ]
        return round(sum(vals) / len(vals), 4) if vals else 0.0

    # Judge pass rate = fraction of queries where overall >= 4 (4 or 5).
    judge_passes = sum(
        1 for ev in evs
        if float((ev.get("judge_breakdown") or {}).get("overall", 0)) >= 4
    )
    hallu_resists = sum(
        1 for ev in evs
        if float((ev.get("judge_breakdown") or {}).get("hallucination", 0)) >= 4
    )

    # Distinct failure-reason counts (e.g. "hallucination": 3, "missed_topology": 5).
    failure_counts: dict[str, int] = {}
    for ev in evs:
        for r in ev.get("failure_reasons") or []:
            failure_counts[r] = failure_counts.get(r, 0) + 1

    return {
        "n_evaluated":              n,
        "avg_judge_overall":        _avg("judge_breakdown", "overall"),
        "avg_judge_relevance":      _avg("judge_breakdown", "relevance"),
        "avg_judge_accuracy":       _avg("judge_breakdown", "accuracy"),
        "avg_judge_completeness":   _avg("judge_breakdown", "completeness"),
        "avg_judge_hallucination":  _avg("judge_breakdown", "hallucination"),
        "avg_judge_clarity":        _avg("judge_breakdown", "clarity"),
        "judge_pass_rate":          round(judge_passes / n, 4),
        "hallucination_resistance_rate": round(hallu_resists / n, 4),
        "avg_entity_f1":            _avg_entity("f1"),
        "avg_entity_precision":     _avg_entity("precision"),
        "avg_entity_recall":        _avg_entity("recall"),
        "avg_entity_path_coverage": _avg_entity("path_coverage"),
        "avg_semantic_score":       _avg("semantic_score"),
        "avg_accuracy":             _avg("accuracy"),
        "failure_counts":           failure_counts,
    }
